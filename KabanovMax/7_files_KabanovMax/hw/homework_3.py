my_food = ['pizza', 'burger', 'sausages', 'beer', 'cake']

print("First three items in the list are: ")
print(my_food[:3])
print("\n")

print("Middle of the list are:")
print(my_food[1:4])
print("\n")

my_food = my_food[::-1]
print(my_food)
print("\n")

print("Last three items are:")
print(my_food[-3:])