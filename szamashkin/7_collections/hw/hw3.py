my_food = ['pizza', 'coca-cola', 'sausages', 'cheesecake', 'ice cream']
print("The first three items in the list are:")
print(my_food[:3])
print("Three items from the middle of the list are:")
print(my_food[1:4])
my_food = my_food[::-1]
print(my_food)
print("The last three items in the list are:")
print(my_food[-3:])
